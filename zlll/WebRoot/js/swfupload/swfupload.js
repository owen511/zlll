//swfupload获取的文件大小是以字节来表示的，用这个方法将其转换为M
//如果文件小于1K（该大小为文本文件只含一个数字字符情况下的大小）不进行转换，显示"小于1K"
SWFUpload.prototype.getFileSize  = function(size,pattern){
	if(size == 0 ){
		return "0K";
	}
	if(size<10000){
		return "小于10K";
	}
	var tmpSize;
	if(typeof(pattern)!=null&&pattern == "KB"){
		tmpSize = size/1024 +"K";
		if(tmpSize.indexOf(".")>0){
			tmpSize = tmpSize.substr(0,(tmpSize.indexOf(".")+3))+"K";
		}
	}else{
		tmpSize = size/1024/1024 +"K";
		if(tmpSize.indexOf(".")>0){
			tmpSize = tmpSize.substr(0,(tmpSize.indexOf(".")+3))+"M";
		}
	}
	return tmpSize;
}
