//加载fileArray对需要下载的文件创建fileprogress
SWFUpload.prototype.loadFileDownloadProgress = function(){
	if(this.settings.fileArray == null){
		return;
	}
	var fileArray = this.settings.fileArray;
	for(var i=0;i<fileArray.length;i++){
		fileArray[i].id = "self_file_"+i; //定义文件ID
		this["progress"+i] = new FileProgress(fileArray[i], this.customSettings.progressTarget);
		this["progress"+i].setStatus("请选择下载或删除！");
		this["progress"+i].setImgStatus("img_"+fileArray[i].id,2);
		this["progress"+i].setProgressBar(fileArray[i].id,100);
		this["progress"+i].setFileGUID(fileArray[i].GUID);
		this["progress"+i].toggleCancel(false, this ,true);//第三个参数控制自定义删除方法，不走框架的方法.
	}
}

// Private: takes a URL, determines if it is relative and converts to an absolute URL
// using the current site. Only processes the URL if it can, otherwise returns the URL untouched
SWFUpload.completeURL = function(url) {
	if (typeof(url) !== "string" || url.match(/^https?:\/\//i) || url.match(/^\//)) {
		return url;
	}
	
	var currentURL = window.location.protocol + "//" + window.location.hostname + (window.location.port ? ":" + window.location.port : "");
	
	var indexSlash = window.location.pathname.lastIndexOf("/");
	if (indexSlash <= 0) {
		path = "/";
	} else {
		path = window.location.pathname.substr(0, indexSlash) + "/";
	}
	
	return /*currentURL +*/ path + url;
	
};


