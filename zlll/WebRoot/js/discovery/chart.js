function multiselectEnabled1(){
    if (document.getElementById("bing").checked) {
		document.getElementById("bing1").disabled=false;
		document.getElementById("bing2").disabled=false;
	} else {
	    document.getElementById("bing1").disabled=true;
		document.getElementById("bing2").disabled=true;
	}
}

